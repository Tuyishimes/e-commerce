package SHOP.SHOP.dto;

public class ContractRequest {
    private String contractRefName;
    private String contractRefNumber;

    public String getContractRefName() {
        return contractRefName;
    }

    public void setContractRefName(String contractRefName) {
        this.contractRefName = contractRefName;
    }

    public String getContractRefNumber() {
        return contractRefNumber;
    }

    public void setContractRefNumber(String contractRefNumber) {
        this.contractRefNumber = contractRefNumber;
    }
}
